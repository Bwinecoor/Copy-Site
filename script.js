function clearName() {
    if (document.getElementById('nameField').value == 'Your Name'){
    document.getElementById('nameField').value = '';
}
}
function resetName() {
    if (document.getElementById('nameField').value == ''){
    document.getElementById('nameField').value = 'Your Name';
}
}
